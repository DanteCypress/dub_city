import React from "react";
import { Link } from "react-router-dom";
import GSW from "../../Resources/images/logos/GSW.png";
import GSWB from "../../Resources/images/logos/GSWB.png";

export const GSWLogo = props => {
  const template = (
    <div
      className="img_cover"
      style={{
        width: props.width,
        height: props.height,
        background: `url(${GSW}) no-repeat`
      }}
    />
  );

  if (props.link) {
    return (
      <Link to={props.linkTo} className="link_logo">
        {template}
      </Link>
    );
  } else {
    return template;
  }
};

